# howSum Problem:
# Given a target sum and a list of numbers, return any combination of numbers
# from the list that add up exactly to the target sum. 
# You can use an element of the list as many times as needed.
# If there is no combination that adds up to the target sum, return None.
#
# Example:
# howSum(7, [5, 3, 4, 7]) -> [3, 4] or [7]
# howSum(7, [2, 4])       -> None